di AS (SELECT *, 100*AVG(pdm) OVER w/NULLIF(AVG(tr) OVER w,0) AS pdi, 100*AVG(ndm) OVER w/NULLIF(AVG(tr) OVER w,0) AS ndi FROM dm WINDOW w AS (PARTITION BY MARKET_TYPE, STOCK_CODE, seg ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW)),
dx AS (SELECT *, 100*ABS(pdi-ndi)/NULLIF(pdi+ndi,0) AS dx FROM di)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'ADX_14',    AVG(dx) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE, seg ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW), NOW() FROM dx WHERE rn >= 100 UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'PLUS_DI_14',  pdi, NOW() FROM dx WHERE rn >= 100 UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'MINUS_DI_14', ndi, NOW() FROM dx WHERE rn >= 100;

