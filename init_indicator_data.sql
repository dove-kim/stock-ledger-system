-- ─────────────────────────────────────────
-- 3. Stochastic %K/%D
--    %K = (close - MIN_LOW_14) / (MAX_HIGH_14 - MIN_LOW_14) * 100
--    %D = AVG(K, 7)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         CLOSE_PRICE,
         MIN(LOW_PRICE)  OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS low14,
         MAX(HIGH_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS high14
  FROM DAILY_STOCK_PRICE
),
stoch AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         CASE WHEN high14 = low14 THEN 50.0
              ELSE (CLOSE_PRICE - low14) / (high14 - low14) * 100
         END AS k_raw
  FROM base
),
with_d AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, k_raw,
         AVG(k_raw) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 6 PRECEDING AND CURRENT ROW) AS d_raw
  FROM stoch
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'STOCHASTIC_K_14_7', k_raw, NOW() FROM with_d WHERE rn >= 20 UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'STOCHASTIC_D_14_7', d_raw, NOW() FROM with_d WHERE rn >= 20;

-- ─────────────────────────────────────────
-- 4. Bollinger Bands
--    BB_MIDDLE = SMA20, BB_UPPER = SMA20 + 2*STDDEV20, BB_LOWER = SMA20 - 2*STDDEV20
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         CLOSE_PRICE,
         AVG(CLOSE_PRICE)        OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW) AS mid,
         STDDEV_POP(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW) AS sd
  FROM DAILY_STOCK_PRICE
),
bb AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, CLOSE_PRICE, mid,
         mid + 2 * sd AS upper_b,
         mid - 2 * sd AS lower_b
  FROM base
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_UPPER_20',     upper_b,                                                                            NOW() FROM bb WHERE rn >= 20 UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_MIDDLE_20',    mid,                                                                                 NOW() FROM bb WHERE rn >= 20 UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_LOWER_20',     lower_b,                                                                             NOW() FROM bb WHERE rn >= 20 UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_PERCENT_B_20', CASE WHEN upper_b = lower_b THEN 0.5 ELSE (CLOSE_PRICE - lower_b) / (upper_b - lower_b) END, NOW() FROM bb WHERE rn >= 20 UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_WIDTH_20',     upper_b - lower_b,                                                                   NOW() FROM bb WHERE rn >= 20;

-- ─────────────────────────────────────────
-- 5. CCI
--    TP = (HIGH + LOW + CLOSE) / 3
--    CCI = (TP - SMA_TP_20) / (0.015 * mean_abs_dev_20)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         (HIGH_PRICE + LOW_PRICE + CLOSE_PRICE) / 3.0 AS tp
  FROM DAILY_STOCK_PRICE
),
sma_tp AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, tp,
         AVG(tp) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW) AS sma_tp
  FROM base
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'CCI',
       (tp - sma_tp) / NULLIF(
         0.015 * AVG(ABS(tp - sma_tp)) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW),
         0
       ),
       NOW()
FROM sma_tp WHERE rn >= 20;

-- ─────────────────────────────────────────
-- 6. Williams %R
--    %R = (MAX_HIGH_14 - close) / (MAX_HIGH_14 - MIN_LOW_14) * -100
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         CLOSE_PRICE,
         MAX(HIGH_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS high14,
         MIN(LOW_PRICE)  OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS low14
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'WILLIAMS_R',
       CASE WHEN high14 = low14 THEN -50.0
            ELSE (high14 - CLOSE_PRICE) / (high14 - low14) * -100
       END,
       NOW()
FROM base WHERE rn >= 14;

-- ─────────────────────────────────────────
-- 7. OBV (On-Balance Volume)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         VOLUME,
         CLOSE_PRICE,
         LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS prev_close
  FROM DAILY_STOCK_PRICE
),
signed_vol AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         CASE WHEN prev_close IS NULL THEN 0
              WHEN CLOSE_PRICE > prev_close THEN  VOLUME
              WHEN CLOSE_PRICE < prev_close THEN -VOLUME
              ELSE 0
         END AS svol
  FROM base
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'OBV',
       SUM(svol) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW),
       NOW()
FROM signed_vol WHERE rn >= 2;

-- ─────────────────────────────────────────
-- 8. Volume Ratio (20)
--    (up_vol + unchanged_vol/2) / (down_vol + unchanged_vol/2) * 100
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         VOLUME,
         CLOSE_PRICE,
         LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS prev_close
  FROM DAILY_STOCK_PRICE
),
tagged AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         CASE WHEN CLOSE_PRICE > prev_close THEN VOLUME ELSE 0 END AS up_vol,
         CASE WHEN CLOSE_PRICE < prev_close THEN VOLUME ELSE 0 END AS dn_vol,
         CASE WHEN CLOSE_PRICE = prev_close THEN VOLUME ELSE 0 END AS eq_vol
  FROM base WHERE prev_close IS NOT NULL
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'VOLUME_RATIO_20', vr, NOW()
FROM (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         (SUM(up_vol) OVER w + SUM(eq_vol) OVER w / 2.0)
         / NULLIF(SUM(dn_vol) OVER w + SUM(eq_vol) OVER w / 2.0, 0) * 100 AS vr
  FROM tagged
  WINDOW w AS (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW)
) t WHERE rn >= 21 AND vr IS NOT NULL;

-- ─────────────────────────────────────────
-- 9. Volume MA20 Ratio
--    volume / AVG(volume, 20)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         VOLUME,
         AVG(VOLUME) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW) AS vol_ma20
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'VOLUME_MA20_RATIO',
       VOLUME / NULLIF(vol_ma20, 0),
       NOW()
FROM base WHERE rn >= 20;

-- ─────────────────────────────────────────
-- 10. Gap Open
--     (open / prev_close) - 1.0
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         OPEN_PRICE,
         LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS prev_close
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'GAP_OPEN',
       OPEN_PRICE / NULLIF(prev_close, 0) - 1.0,
       NOW()
FROM base WHERE rn >= 2;

-- ─────────────────────────────────────────
-- 11. Volatility (log return STDDEV)
--     VOLATILITY_5D  = STDDEV(log daily return, 5)
--     VOLATILITY_20D = STDDEV(log daily return, 20)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         LN(CLOSE_PRICE / NULLIF(LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE), 0)) AS log_ret
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'VOLATILITY_5D',
       STDDEV_POP(log_ret) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 4  PRECEDING AND CURRENT ROW),
       NOW() FROM base WHERE rn >= 21
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'VOLATILITY_20D',
       STDDEV_POP(log_ret) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW),
       NOW() FROM base WHERE rn >= 21;

-- ─────────────────────────────────────────
-- 12. RSI (SMA 근사)
--     avg_gain = AVG(MAX(close-prev,0), N)
--     RSI = 100 - 100 / (1 + avg_gain/avg_loss)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         GREATEST(CLOSE_PRICE - LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE), 0) AS gain,
         GREATEST(LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) - CLOSE_PRICE, 0) AS loss
  FROM DAILY_STOCK_PRICE
),
rsi AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         AVG(gain) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 8  PRECEDING AND CURRENT ROW) AS ag9,
         AVG(loss) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 8  PRECEDING AND CURRENT ROW) AS al9,
         AVG(gain) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS ag14,
         AVG(loss) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS al14,
         AVG(gain) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW) AS ag21,
         AVG(loss) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW) AS al21
  FROM base
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RSI_9',  CASE WHEN al9=0  THEN 100 ELSE 100-100/(1+ag9/al9)   END, NOW() FROM rsi WHERE rn >= 45  UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RSI_14', CASE WHEN al14=0 THEN 100 ELSE 100-100/(1+ag14/al14) END, NOW() FROM rsi WHERE rn >= 70  UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RSI_21', CASE WHEN al21=0 THEN 100 ELSE 100-100/(1+ag21/al21) END, NOW() FROM rsi WHERE rn >= 105;

-- ─────────────────────────────────────────
-- 16. ATR (Average True Range, SMA 근사)
--     TR = MAX(high-low, |high-prevClose|, |low-prevClose|)
--     ATR = AVG(TR, 14)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         GREATEST(
           HIGH_PRICE - LOW_PRICE,
           ABS(HIGH_PRICE - LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE)),
           ABS(LOW_PRICE  - LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE))
         ) AS tr
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'ATR',
       AVG(tr) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW),
       NOW()
FROM base WHERE rn >= 70;

-- ─────────────────────────────────────────
-- 17. MFI (Money Flow Index)
--     TP = (H+L+C)/3, RMF = TP * volume
--     MFI = 100 - 100 / (1 + pos_flow/neg_flow)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         (HIGH_PRICE + LOW_PRICE + CLOSE_PRICE) / 3.0 AS tp,
         (HIGH_PRICE + LOW_PRICE + CLOSE_PRICE) / 3.0 * VOLUME AS rmf,
         LAG((HIGH_PRICE + LOW_PRICE + CLOSE_PRICE) / 3.0) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS prev_tp
  FROM DAILY_STOCK_PRICE
),
flows AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         CASE WHEN tp > prev_tp THEN rmf ELSE 0 END AS pos_mf,
         CASE WHEN tp < prev_tp THEN rmf ELSE 0 END AS neg_mf
  FROM base WHERE prev_tp IS NOT NULL
),
mfi_raw AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         SUM(pos_mf) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS pos_sum,
         SUM(neg_mf) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS neg_sum
  FROM flows
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'MFI',
       CASE
         WHEN neg_sum = 0 AND pos_sum = 0 THEN 50.0
         WHEN neg_sum = 0 THEN 100.0
         ELSE 100 - 100 / (1 + pos_sum / neg_sum)
       END,
       NOW()
FROM mfi_raw WHERE rn >= 15;

-- ─────────────────────────────────────────
-- 18. MACD (SMA 근사: EMA12≈SMA12, EMA26≈SMA26, signal=SMA9(MACD))
--     MACD_LINE      = SMA12 - SMA26
--     MACD_SIGNAL    = SMA9(MACD_LINE)
--     MACD_HISTOGRAM = MACD_LINE - MACD_SIGNAL
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 11 PRECEDING AND CURRENT ROW) AS sma12,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 25 PRECEDING AND CURRENT ROW) AS sma26
  FROM DAILY_STOCK_PRICE
),
macd_line AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, sma12 - sma26 AS macd FROM base
),
macd_sig AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, macd,
