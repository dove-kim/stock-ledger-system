-- ─────────────────────────────────────────
-- 1. SMA (단순이동평균)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 4   PRECEDING AND CURRENT ROW) AS sma5,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 9   PRECEDING AND CURRENT ROW) AS sma10,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19  PRECEDING AND CURRENT ROW) AS sma20,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 49  PRECEDING AND CURRENT ROW) AS sma50,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 59  PRECEDING AND CURRENT ROW) AS sma60,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 119 PRECEDING AND CURRENT ROW) AS sma120,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 199 PRECEDING AND CURRENT ROW) AS sma200
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_5',   sma5,   NOW() FROM base WHERE rn >= 5   UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_10',  sma10,  NOW() FROM base WHERE rn >= 10  UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_20',  sma20,  NOW() FROM base WHERE rn >= 20  UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_50',  sma50,  NOW() FROM base WHERE rn >= 50  UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_60',  sma60,  NOW() FROM base WHERE rn >= 60  UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_120', sma120, NOW() FROM base WHERE rn >= 120 UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_200', sma200, NOW() FROM base WHERE rn >= 200;

