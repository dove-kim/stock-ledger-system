-- ─────────────────────────────────────────
-- 14. RSI (SMA 근사)
--     RSI_9:  requiredDataSize = 45,  period = 9
--     RSI_14: requiredDataSize = 70,  period = 14
--     RSI_21: requiredDataSize = 105, period = 21
--     avg_gain = AVG(MAX(close-prev,0), p)
--     avg_loss = AVG(MAX(prev-close,0), p)
--     RSI = 100 - 100 / (1 + avg_gain/avg_loss)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         GREATEST(CLOSE_PRICE - LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE), 0) AS gain,
         GREATEST(LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) - CLOSE_PRICE, 0) AS loss
  FROM DAILY_STOCK_PRICE
),
rsi AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         AVG(gain) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 8  PRECEDING AND CURRENT ROW) AS ag9,
         AVG(loss) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 8  PRECEDING AND CURRENT ROW) AS al9,
         AVG(gain) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS ag14,
         AVG(loss) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS al14,
         AVG(gain) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW) AS ag21,
         AVG(loss) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW) AS al21
  FROM base
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RSI_9',
       100 - 100 / (1 + ag9  / NULLIF(al9,  0)), NOW() FROM rsi WHERE rn >= 45
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RSI_14',
       100 - 100 / (1 + ag14 / NULLIF(al14, 0)), NOW() FROM rsi WHERE rn >= 70
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RSI_21',
       100 - 100 / (1 + ag21 / NULLIF(al21, 0)), NOW() FROM rsi WHERE rn >= 105;

-- ─────────────────────────────────────────
-- 15. ATR (Average True Range, SMA 근사)
--     requiredDataSize = 70
--     TR = MAX(high-low, |high-prev_close|, |low-prev_close|)
--     ATR = AVG(TR, 14)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         GREATEST(
           HIGH_PRICE - LOW_PRICE,
           ABS(HIGH_PRICE - LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE)),
           ABS(LOW_PRICE  - LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE))
         ) AS tr
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'ATR',
       AVG(tr) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW),
       NOW()
FROM base WHERE rn >= 70;

-- ─────────────────────────────────────────
-- 16. MFI (Money Flow Index)
--     requiredDataSize = 15
--     TP = (H+L+C)/3,  RMF = TP * volume
--     pos_flow = SUM(RMF where TP > prev_TP, 14)
--     neg_flow = SUM(RMF where TP < prev_TP, 14)
--     MFI = 100 - 100 / (1 + pos_flow/neg_flow)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         (HIGH_PRICE + LOW_PRICE + CLOSE_PRICE) / 3.0 AS tp,
         (HIGH_PRICE + LOW_PRICE + CLOSE_PRICE) / 3.0 * VOLUME AS rmf,
         LAG((HIGH_PRICE + LOW_PRICE + CLOSE_PRICE) / 3.0) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS prev_tp
  FROM DAILY_STOCK_PRICE
),
flows AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         CASE WHEN tp > prev_tp THEN rmf ELSE 0 END AS pos_mf,
         CASE WHEN tp < prev_tp THEN rmf ELSE 0 END AS neg_mf
  FROM base WHERE prev_tp IS NOT NULL
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'MFI',
       100 - 100 / (1 +
         SUM(pos_mf) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW)
         / NULLIF(SUM(neg_mf) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW), 0)
       ),
       NOW()
FROM flows WHERE rn >= 15;

-- ─────────────────────────────────────────
-- 17. MACD (SMA 근사: EMA12≈SMA12, EMA26≈SMA26, signal=SMA9(MACD))
--     requiredDataSize = 130
--     MACD_LINE      = SMA12 - SMA26
--     MACD_SIGNAL    = SMA9(MACD_LINE)
--     MACD_HISTOGRAM = MACD_LINE - MACD_SIGNAL
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 11 PRECEDING AND CURRENT ROW) AS sma12,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 25 PRECEDING AND CURRENT ROW) AS sma26
  FROM DAILY_STOCK_PRICE
),
macd_line AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         sma12 - sma26 AS macd
  FROM base
),
macd_signal AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, macd,
         AVG(macd) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 8 PRECEDING AND CURRENT ROW) AS signal
  FROM macd_line
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'MACD_LINE',      macd,            NOW() FROM macd_signal WHERE rn >= 130
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'MACD_SIGNAL',    signal,          NOW() FROM macd_signal WHERE rn >= 130
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'MACD_HISTOGRAM', macd - signal,   NOW() FROM macd_signal WHERE rn >= 130;

-- ─────────────────────────────────────────
-- 18. ADX / +DI / -DI (SMA 근사)
--     requiredDataSize = 100
--     TR  = MAX(H-L, |H-prevC|, |L-prevC|)
--     +DM = MAX(H - prevH, 0) if (H-prevH) > (prevL-L) else 0
--     -DM = MAX(prevL - L, 0) if (prevL-L) > (H-prevH) else 0
--     +DI = 100 * AVG(+DM,14) / AVG(TR,14)
--     -DI = 100 * AVG(-DM,14) / AVG(TR,14)
--     DX  = 100 * |+DI - -DI| / (+DI + -DI)
--     ADX = AVG(DX, 14)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         HIGH_PRICE  AS h, LOW_PRICE AS l, CLOSE_PRICE AS c,
         LAG(HIGH_PRICE)  OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS ph,
         LAG(LOW_PRICE)   OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS pl,
         LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS pc
  FROM DAILY_STOCK_PRICE
),
dm AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         GREATEST(h - l, ABS(h - pc), ABS(l - pc)) AS tr,
         CASE WHEN (h - ph) > (pl - l) AND (h - ph) > 0 THEN h - ph ELSE 0 END AS pdm,
         CASE WHEN (pl - l) > (h - ph) AND (pl - l) > 0 THEN pl - l ELSE 0 END AS ndm
  FROM base WHERE pc IS NOT NULL
),
di AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         100 * AVG(pdm) OVER w / NULLIF(AVG(tr) OVER w, 0) AS pdi,
         100 * AVG(ndm) OVER w / NULLIF(AVG(tr) OVER w, 0) AS ndi
  FROM dm
  WINDOW w AS (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW)
),
dx AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, pdi, ndi,
         100 * ABS(pdi - ndi) / NULLIF(pdi + ndi, 0) AS dx
  FROM di
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'ADX',
       AVG(dx) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW),
       NOW() FROM dx WHERE rn >= 100
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'PLUS_DI',  pdi, NOW() FROM dx WHERE rn >= 100
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'MINUS_DI', ndi, NOW() FROM dx WHERE rn >= 100;

-- ─────────────────────────────────────────
-- 19. INDICATOR_CURSOR
--     각 종목·지표 타입별 마지막 계산 날짜 = 해당 종목의 마지막 TRADE_DATE
--     requiredDataSize 미달 종목·지표는 제외
--     cursorType 기준 (calculator당 1개):
--       SMA_5/10/20/50/60/120/200, RSI_9/14/21, MACD_LINE,
--       STOCHASTIC_K, ADX, BB_UPPER, ATR, OBV, MFI, CCI,
--       WILLIAMS_R, VOLUME_RATIO, RETURN_1D, VOLATILITY_5D,
--       MA_DEVIATION_20, MA5_MA20_RATIO, VOLUME_MA20_RATIO, GAP_OPEN
-- ─────────────────────────────────────────
INSERT INTO INDICATOR_CURSOR (MARKET_TYPE, CODE, INDICATOR_TYPE, LAST_CALCULATED_DATE)
SELECT t.MARKET_TYPE, t.STOCK_CODE, t.INDICATOR_NAME, t.TRADE_DATE
FROM TECHNICAL_INDICATOR t
INNER JOIN (
  SELECT MARKET_TYPE, STOCK_CODE, INDICATOR_NAME, MAX(TRADE_DATE) AS TRADE_DATE
  FROM TECHNICAL_INDICATOR
  WHERE INDICATOR_NAME IN (
    'SMA_5','SMA_10','SMA_20','SMA_50','SMA_60','SMA_120','SMA_200',
    'RSI_9','RSI_14','RSI_21',
    'MACD_LINE',
    'STOCHASTIC_K',
    'ADX',
    'BB_UPPER',
    'ATR',
    'OBV',
    'MFI',
    'CCI',
    'WILLIAMS_R',
    'VOLUME_RATIO',
    'RETURN_1D',
    'VOLATILITY_5D',
    'MA_DEVIATION_20',
    'MA5_MA20_RATIO',
    'VOLUME_MA20_RATIO',
    'GAP_OPEN'
  )
  GROUP BY MARKET_TYPE, STOCK_CODE, INDICATOR_NAME
) latest USING (MARKET_TYPE, STOCK_CODE, INDICATOR_NAME, TRADE_DATE);
