-- ─────────────────────────────────────────
-- 10. Returns (log return)
--     requiredDataSize = 21
--     RETURN_ND = LN(close / LAG(close, N))
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         CLOSE_PRICE,
         LAG(CLOSE_PRICE, 1)  OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS c1,
         LAG(CLOSE_PRICE, 3)  OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS c3,
         LAG(CLOSE_PRICE, 5)  OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS c5,
         LAG(CLOSE_PRICE, 10) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS c10,
         LAG(CLOSE_PRICE, 20) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS c20
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RETURN_1D',  LN(CLOSE_PRICE / NULLIF(c1,  0)), NOW() FROM base WHERE rn >= 21
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RETURN_3D',  LN(CLOSE_PRICE / NULLIF(c3,  0)), NOW() FROM base WHERE rn >= 21
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RETURN_5D',  LN(CLOSE_PRICE / NULLIF(c5,  0)), NOW() FROM base WHERE rn >= 21
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RETURN_10D', LN(CLOSE_PRICE / NULLIF(c10, 0)), NOW() FROM base WHERE rn >= 21
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'RETURN_20D', LN(CLOSE_PRICE / NULLIF(c20, 0)), NOW() FROM base WHERE rn >= 21;

-- ─────────────────────────────────────────
-- 11. Volatility (log return STDDEV)
--     requiredDataSize = 21
--     VOLATILITY_5D  = STDDEV(log daily return, 5)
--     VOLATILITY_20D = STDDEV(log daily return, 20)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         LN(CLOSE_PRICE / NULLIF(LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE), 0)) AS log_ret
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'VOLATILITY_5D',
       STDDEV_POP(log_ret) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 4  PRECEDING AND CURRENT ROW),
       NOW() FROM base WHERE rn >= 21
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'VOLATILITY_20D',
       STDDEV_POP(log_ret) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW),
       NOW() FROM base WHERE rn >= 21;

