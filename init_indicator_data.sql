-- ─────────────────────────────────────────
-- 1. SMA (단순이동평균)
--    SMA_N: requiredDataSize = N
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 4   PRECEDING AND CURRENT ROW) AS sma5,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 9   PRECEDING AND CURRENT ROW) AS sma10,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19  PRECEDING AND CURRENT ROW) AS sma20,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 49  PRECEDING AND CURRENT ROW) AS sma50,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 59  PRECEDING AND CURRENT ROW) AS sma60,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 119 PRECEDING AND CURRENT ROW) AS sma120,
         AVG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 199 PRECEDING AND CURRENT ROW) AS sma200
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_5',   sma5,   NOW() FROM base WHERE rn >= 5
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_10',  sma10,  NOW() FROM base WHERE rn >= 10
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_20',  sma20,  NOW() FROM base WHERE rn >= 20
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_50',  sma50,  NOW() FROM base WHERE rn >= 50
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_60',  sma60,  NOW() FROM base WHERE rn >= 60
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_120', sma120, NOW() FROM base WHERE rn >= 120
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'SMA_200', sma200, NOW() FROM base WHERE rn >= 200;

-- ─────────────────────────────────────────
-- 2. Stochastic %K/%D
--    requiredDataSize = 20
--    %K = (close - MIN_LOW_14) / (MAX_HIGH_14 - MIN_LOW_14) * 100
--    %D = AVG(K, 7)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         CLOSE_PRICE,
         MIN(LOW_PRICE)  OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS low14,
         MAX(HIGH_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS high14
  FROM DAILY_STOCK_PRICE
),
stoch AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         CASE WHEN high14 = low14 THEN 50.0
              ELSE (CLOSE_PRICE - low14) / (high14 - low14) * 100
         END AS k_raw
  FROM base
),
with_d AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, k_raw,
         AVG(k_raw) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 6 PRECEDING AND CURRENT ROW) AS d_raw
  FROM stoch
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'STOCHASTIC_K', k_raw, NOW() FROM with_d WHERE rn >= 20
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'STOCHASTIC_D', d_raw, NOW() FROM with_d WHERE rn >= 20;

-- ─────────────────────────────────────────
-- 3. Bollinger Bands
--    requiredDataSize = 20
--    BB_MIDDLE = SMA20, BB_UPPER = SMA20 + 2*STDDEV20, BB_LOWER = SMA20 - 2*STDDEV20
--    BB_PERCENT_B = (close - lower) / (upper - lower)
--    BB_WIDTH = upper - lower
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         CLOSE_PRICE,
         AVG(CLOSE_PRICE)        OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW) AS mid,
         STDDEV_POP(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW) AS sd
  FROM DAILY_STOCK_PRICE
),
bb AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, CLOSE_PRICE, mid,
         mid + 2 * sd AS upper_b,
         mid - 2 * sd AS lower_b
  FROM base
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_UPPER',     upper_b, NOW() FROM bb WHERE rn >= 20
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_MIDDLE',    mid,     NOW() FROM bb WHERE rn >= 20
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_LOWER',     lower_b, NOW() FROM bb WHERE rn >= 20
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_PERCENT_B',
       CASE WHEN upper_b = lower_b THEN 0.5 ELSE (CLOSE_PRICE - lower_b) / (upper_b - lower_b) END,
       NOW() FROM bb WHERE rn >= 20
UNION ALL
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'BB_WIDTH', upper_b - lower_b, NOW() FROM bb WHERE rn >= 20;

-- ─────────────────────────────────────────
-- 4. CCI
--    requiredDataSize = 20
--    TP = (HIGH + LOW + CLOSE) / 3
--    CCI = (TP - SMA_TP_20) / (0.015 * mean_abs_dev_20)
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         (HIGH_PRICE + LOW_PRICE + CLOSE_PRICE) / 3.0 AS tp
  FROM DAILY_STOCK_PRICE
),
sma_tp AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn, tp,
         AVG(tp) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW) AS sma_tp
  FROM base
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'CCI',
       (tp - sma_tp) / NULLIF(
         0.015 * AVG(ABS(tp - sma_tp)) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 19 PRECEDING AND CURRENT ROW),
         0
       ),
       NOW()
FROM sma_tp WHERE rn >= 20;

-- ─────────────────────────────────────────
-- 5. Williams %R
--    requiredDataSize = 14
--    %R = (MAX_HIGH_14 - close) / (MAX_HIGH_14 - MIN_LOW_14) * -100
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         CLOSE_PRICE,
         MAX(HIGH_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS high14,
         MIN(LOW_PRICE)  OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 13 PRECEDING AND CURRENT ROW) AS low14
  FROM DAILY_STOCK_PRICE
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'WILLIAMS_R',
       CASE WHEN high14 = low14 THEN -50.0
            ELSE (high14 - CLOSE_PRICE) / (high14 - low14) * -100
       END,
       NOW()
FROM base WHERE rn >= 14;

-- ─────────────────────────────────────────
-- 6. OBV (On-Balance Volume)
--    requiredDataSize = 2
--    OBV += vol if close > prev_close, -= vol if close < prev_close, else unchanged
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         VOLUME,
         CLOSE_PRICE,
         LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS prev_close
  FROM DAILY_STOCK_PRICE
),
signed_vol AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         CASE WHEN prev_close IS NULL THEN 0
              WHEN CLOSE_PRICE > prev_close THEN  VOLUME
              WHEN CLOSE_PRICE < prev_close THEN -VOLUME
              ELSE 0
         END AS svol
  FROM base
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'OBV',
       SUM(svol) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN UNBOUNDED PRECEDING AND CURRENT ROW),
       NOW()
FROM signed_vol WHERE rn >= 2;

-- ─────────────────────────────────────────
-- 7. Volume Ratio
--    requiredDataSize = 21
--    (up_vol + unchanged_vol/2) / (down_vol + unchanged_vol/2) * 100
-- ─────────────────────────────────────────
INSERT INTO TECHNICAL_INDICATOR
  (MARKET_TYPE, STOCK_CODE, TRADE_DATE, INDICATOR_NAME, INDICATOR_VALUE, CALCULATED_AT)
WITH base AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE,
         ROW_NUMBER() OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS rn,
         VOLUME,
         CLOSE_PRICE,
         LAG(CLOSE_PRICE) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE) AS prev_close
  FROM DAILY_STOCK_PRICE
),
tagged AS (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         CASE WHEN CLOSE_PRICE > prev_close THEN VOLUME ELSE 0 END AS up_vol,
         CASE WHEN CLOSE_PRICE < prev_close THEN VOLUME ELSE 0 END AS dn_vol,
         CASE WHEN CLOSE_PRICE = prev_close THEN VOLUME ELSE 0 END AS eq_vol
  FROM base WHERE prev_close IS NOT NULL
)
SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, 'VOLUME_RATIO', vr, NOW()
FROM (
  SELECT MARKET_TYPE, STOCK_CODE, TRADE_DATE, rn,
         (SUM(up_vol) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW)
          + SUM(eq_vol) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW) / 2.0)
         / NULLIF(
             SUM(dn_vol) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW)
             + SUM(eq_vol) OVER (PARTITION BY MARKET_TYPE, STOCK_CODE ORDER BY TRADE_DATE ROWS BETWEEN 20 PRECEDING AND CURRENT ROW) / 2.0,
           0) * 100 AS vr
  FROM tagged
) t WHERE rn >= 21;

